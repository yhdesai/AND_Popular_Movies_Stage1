package io.github.yhdesai.PopularMovies.model;

public class MovieVideo {
    private String vID;
    private String vISO639;
    private String vISO3166;
    private String vKey;
    private String vName;
    private String vSite;
    private int vSize;
    private String vType;


    public String getvID() {
        return vID;
    }

    public void setvID(String vID) {
        this.vID = vID;
    }

    public String getvISO639() {
        return vISO639;
    }

    public void setvISO639(String vISO639) {
        this.vISO639 = vISO639;
    }

    public String getvISO3166() {
        return vISO3166;
    }

    public void setvISO3166(String vISO3166) {
        this.vISO3166 = vISO3166;
    }

    public String getvKey() {
        return vKey;
    }

    public void setvKey(String vKey) {
        this.vKey = vKey;
    }

    public String getvName() {
        return vName;
    }

    public void setvName(String vName) {
        this.vName = vName;
    }

    public String getvSite() {
        return vSite;
    }

    public void setvSite(String vSite) {
        this.vSite = vSite;
    }

    public int getvSize() {
        return vSize;
    }

    public void setvSize(int vSize) {
        this.vSize = vSize;
    }

    public String getvType() {
        return vType;
    }

    public void setvType(String vType) {
        this.vType = vType;
    }
}
